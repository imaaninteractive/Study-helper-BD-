  const text = question.toLowerCase();
  const intents = JOSEN_MODELS[model].intents;

  let bestIntent = null;
  let maxScore = 0;

  for (const intent of intents) {
    let score = 0;
    for (const keyword of intent.keywords) {
      if (text.includes(keyword)) score++;
    }
    if (score > maxScore) {
      maxScore = score;
      bestIntent = intent;
    }
  }

  if (!bestIntent) return "ржЖржорж┐ ржПржЗ ржкрзНрж░рж╢рзНржиржЯрж╛ ржПржЦржиржУ рж╢рж┐ржЦрж┐ржирж┐ ЁЯЩВ";

